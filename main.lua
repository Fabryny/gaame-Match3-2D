
love.graphics.setDefaultFilter('nearest', 'nearest')
require 'src/Dependencies'


-- physical screen 
WINDOW_WIDTH = 1280
WINDOW_HEIGHT = 720

-- virtual resolution 
VIRTUAL_WIDTH = 512
VIRTUAL_HEIGHT = 288


BACKGROUND_SCROLL_SPEED = 80

function love.load()
    love.window.setTitle('Match 3')

    -- seed the RNG
    math.randomseed(os.time())

    push:setupScreen(VIRTUAL_WIDTH, VIRTUAL_HEIGHT, WINDOW_WIDTH, WINDOW_HEIGHT, {
        vsync = true,
        fullscreen = false,
        resizable = true,
        canvas = true
    })

    gSounds['music']:setLooping(true)
    gSounds['music']:play()

    gStateMachine = StateMachine {
        ['start'] = function() return StartState() end,
        ['begin-game'] = function() return BeginGameState() end,
        ['play'] = function() return PlayState() end,
        ['game-over'] = function() return GameOverState() end
    }
    gStateMachine:change('start')

    -- keep track of scrolling our background on the X axis
    backgroundX = 0

    love.keyboard.keysPressed = {}
end


function love.resize(w, h)
    push:resize(w, h)
end

function love.keypressed(key)
    -- add to our table of keys pressed this frame
    love.keyboard.keysPressed[key] = true
end


function love.keyboard.wasPressed(key)
    if love.keyboard.keysPressed[key] then
        return true
    else
        return false
    end
end


function love.update(dt)

    backgroundX = backgroundX - BACKGROUND_SCROLL_SPEED * dt
    
    -- Se a imagem tiver ido ao maximo, resetar p/ 0
    if backgroundX <= -1024 + VIRTUAL_WIDTH - 4 + 51 then
        backgroundX = 0
    end

    gStateMachine:update(dt)

    love.keyboard.keysPressed = {}
end

function love.draw()
    push:start()

    -- scrolling background drawn behind every state
    love.graphics.draw(gTextures['background'], backgroundX, 0)
    
    gStateMachine:render()
    push:finish()
end


